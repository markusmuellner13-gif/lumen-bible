const lookup = (lang, id) => JSON.parse(readFileSync(here(`../data/${lang}/${id}.json`), 'utf8')).chapters;
const cache = { de: {}, en: {} };
const getText = (lang, id, c, v) => {
  cache[lang][id] ||= lookup(lang, id);
  return cache[lang][id][c]?.[v];
};
const manifest = JSON.parse(readFileSync(here('../data/manifest.json'), 'utf8'));
const meta = (id) => manifest.books.find((b) => b.id === id);

const devotions = [];
let bad = 0;
D.forEach((d, i) => {
  const [id, c, v, theme, deT, enT, deR, enR, deP, enP] = d;
  const de = getText('de', id, c, v), en = getText('en', id, c, v);
  if (!de || !en) { console.warn('MISSING TEXT', id, c, v, 'de', !!de, 'en', !!en); bad++; return; }
  const m = meta(id);
  devotions.push({
    n: i, book: id, c: +c, v: +v, theme,
    ref: { de: `${m.de.name} ${c},${v}`, en: `${m.en.name} ${c}:${v}` },
    text: { de, en }, title: { de: deT, en: enT },
    reflection: { de: deR, en: enR }, prayer: { de: deP, en: enP },
  });
});

writeFileSync(here('../data/devotions.json'), JSON.stringify({ version: 1, devotions }, null, 0));
console.log(`Devotions written: ${devotions.length} (missing: ${bad})`);
if (bad) process.exit(1);
